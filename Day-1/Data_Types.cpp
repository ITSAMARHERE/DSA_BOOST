#include <iostream>
using namespace std;

int main() {
    int a = 123;
    cout << "Value of a : "<< a << endl;

    char b = 'F';
    cout << "Value of b : " << b << endl;

    bool bl = true;
    cout << "Value of bl : " << bl << endl;

    float f = 1.2;
    cout << "Value of f : " << f << endl;

    double d = 1.234;
    cout << "Value of d : " << d << endl;

    int size = sizeof(a);
    cout << "Size of a : " << size << endl;
}